package main

import "fmt" // Пакет для форматированного ввода/вывода

// DeleteI - функция для удаления элемента из среза по индексу
func DeleteI(s []int, i int) []int {
	// newS - новый срез для хранения результата (без удаленного элемента)
	newS := make([]int, 0, len(s)-1) // Инициализация пустого среза с начальной емкостью (len(s)-1)

	// Проверка корректности индекса
	if i < 0 || i >= len(s) { // Индекс выходит за пределы среза
		return nil // Возврат nil в случае ошибки
	}

	// Специальные случаи для удаления первого или последнего элемента
	if i == 0 {
		if len(s) > 1 { // Удаление первого элемента, если срез не пустой
			return s[1:] // Возврат среза начиная со второго элемента
		}
		return nil // Возврат nil, если срез пустой
	}

	if i == len(s)-1 { // Удаление последнего элемента
		return s[:len(s)-1] // Возврат среза до последнего элемента
	}

	// Удаление элемента из середины среза
	newS = append(newS, s[0:i]...)  // Добавление элементов до удаляемого элемента в newS
	newS = append(newS, s[i+1:]...) // Добавление элементов после удаляемого элемента в newS

	return newS // Возврат нового среза без удаленного элемента
}

func main() {
	// Исходный срез
	a := []int{3, 4, 432, 324, 5, 3, 1000, 4}

	// Удаление элемента с индексом 5 (значение 5)
	result := DeleteI(a, 5)

	// Печать результата (срез без элемента 5)
	fmt.Print(result)
}
